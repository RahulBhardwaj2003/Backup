public class IfElseStatement {
    public static void main(String[] args) {      
        int a = 10;       
        if (a % 2 == 0)     {           
            System.out.println(a + " is an even number");       
        }       
        else     {           
            System.out.println(a + " is an odd number");         
        }   
    }
}
