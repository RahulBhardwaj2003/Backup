public class LogicalNOToperator {
    public static void main(String[] args) {
        int a = 85, b = 39;
        
        System.out.println("var a = " +a);
        System.out.println("var b = " +b);

        System.out.println("!(a<b) " +!(a<b));
        System.out.println("!(b<a) " +!(b<a));
    }
}
