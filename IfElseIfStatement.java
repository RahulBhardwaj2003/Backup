public class IfElseIfStatement {
    public static void main(String[] args) {       
        int i = 3;       
        if (i == 1) {        
             System.out.println("January");       
        } else if (i == 2) {          
             System.out.println("February");       
        } else if (i == 3) {       
             System.out.println("March");       
        } else {        
             System.out.println("April");         
    }
}
}
